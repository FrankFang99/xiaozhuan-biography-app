  onAvatarError: function(e) {
    const id = e.currentTarget.dataset.id
    const stories = this.data.stories.map(story => {
      if (story.id === id) {
        return { ...story, avatarImage: '' }
      }
      return story
    })
    this.setData({ stories })
  },

  onModalAvatarError: function() {
    if (this.data.selectedStory) {
      this.setData({
        'selectedStory.avatarImage': ''
      })
    }
  }
