  onLoad: function(options) {
    this.initStars()
    
    const userBiography = wx.getStorageSync('biography')
    if (userBiography && userBiography.id === 'user_biography') {
      const existingIndex = this.data.biographies.findIndex(b => b.id === 'user_biography')
      if (existingIndex >= 0) {
        const newBiographies = [...this.data.biographies]
        newBiographies[existingIndex] = userBiography
        this.setData({ biographies: newBiographies })
      } else {
        this.setData({ biographies: [...this.data.biographies, userBiography] })
      }
    }
    
    if (options && options.id) {
      const biography = this.data.biographies.find(b => b.id === options.id)
      if (biography) {
        this.setData({ currentBiography: biography, viewMode: 'cover' })
      }
    }
  },

  initStars: function() {
    const stars = []
    for (let i = 0; i < 50; i++) {
      stars.push({
        id: 'star_' + i,
        x: Math.random() * 100,
        y: Math.random() * 100,
        opacity: Math.random() * 0.6 + 0.2,
        duration: Math.random() * 5 + 3,
        delay: Math.random() * 3,
        size: Math.random() * 16 + 12
      })
    }
    this.setData({ bgStars: stars })
  },

  openBook: function() {
    this.setData({ viewMode: 'content' })
  },

  closeBook: function() {
    this.setData({ viewMode: 'universe', currentBiography: null })
  },

