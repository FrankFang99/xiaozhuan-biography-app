  onAvatarError: function(e) {
    const type = e.currentTarget.dataset.type
    if (this.data.selectedStar && type === 'popup') {
      this.setData({
        'selectedStar.avatarImage': ''
      })
    }
    if (this.data.currentBiography && type === 'cover') {
      this.setData({
        'currentBiography.avatarImage': ''
      })
    }
  }
